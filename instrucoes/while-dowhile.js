// While: É executado sempre que uma condição for verdadeira.

let c = 1;

while(c <= 10) {
    console.log(c);
    c++; // Incremento para o loop não ser infinito
}



// Do while: O DO é executado pelo menos uma vez e depois faz uma verificação para verificar se a condição é verdadeira.

let t = 1;

do {
    console.log(t);
    t++; // Incremento para o loop não ser infinito
} while (t < 6);